
class Travel {
  final String id;
  final String arrivalPoint;
  final String startingPoint;
  final String driver;
  final List passengers;
  final String price;
  final int seats;
  final String hour;
  final String date;

  Travel({
    required this.id,
    required this.arrivalPoint,
    required this.startingPoint,
    required this.driver,
    required this.passengers,
    required this.price,
    required this.seats,
    required this.hour,
    required this.date
  });
}
